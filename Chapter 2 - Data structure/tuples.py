# Code to write the tuples 

t1 = (47, 41, 23, 85)

# to count the specific tuple in tuple list 
print(t1.count(41))

# to find the index of particular entry in tuple list 
print(t1.index(23))

# to find the length of tuple 
print(len(t1))

# to find max value in tuple list 
print(max(t1))


# to find minimum value in tuple list 
print(min(t1))

# to sort the tuple list 
print(sorted(t1))